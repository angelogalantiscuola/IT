'''
Conversion from int, string, list to bool
'''

integer = 0
print(bool(integer))
integer = -33
print(bool(integer))
integer = 0
print(bool(integer))

string = "hello"
print(bool(string))
string = ""
print(bool(string))

list = [1, 2, 3]
print(bool(list))
list = []
print(bool(list))